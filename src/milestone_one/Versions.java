package milestone_one;

public class Versions {
	
	int iv;
	int ov;
	int fv;
	String key;
	
	public int getIv() {
		return iv;
	}
	public void setIv(int iv) {
		this.iv = iv;
	}
	public int getOv() {
		return ov;
	}
	public void setOv(int ov) {
		this.ov = ov;
	}
	public int getFv() {
		return fv;
	}
	public void setFv(int fv) {
		this.fv = fv;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
	

}
